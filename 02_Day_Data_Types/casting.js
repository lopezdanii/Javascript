// se usan parseInt(), parseFloat(), Number(), +sign, str()

//string to int
let num = '10'
let numInt = parseInt(num)
console.log(numInt) // 10

numInt = Number(num)
console.log(numInt) // 10

numInt = +num
console.log(numInt) // 10


//string to float
let num2 = '12.55'
let numFloat = parseFloat(num2)
console.log(numFloat) // 10

numFloat = Number(num2)
console.log(numFloat) // 10

numFloat = +num2
console.log(numFloat) // 10
